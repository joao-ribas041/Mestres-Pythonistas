# Tipos de números que podem ser usados no Python
a = 20
b = 20.5
print(type(a))
print(type(b))

print(10 + 6)
print(10 - 6)
print(10 * 6)
print(10 / 6)
print(10 // 6)  # divisão de inteiro
print(10 % 6)  # modulos
print(10 ** 6)  # exponenciais

