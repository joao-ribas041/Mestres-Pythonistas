# Pq que podemos comparar?

# True ou False
idade = 15
print(idade > 17)  # Maior que
print(idade < 17)  # Menor que
print(idade <= 17)  # Menor ou igual que
print(idade >= 17)  # Maior ou igual que
print(idade == 17)  # Igual a
print(idade != 17)  # Diferente de

# Comparações entre outros tipos
print(True == True)
print('Rafael' == 'rafael')
print('b' > 'a')
print(5 == '5')  # Falso (int == str)
